char	*ft_strcat(char *s1, const char *s2)
{
	int	count;
	int	x;

	x = 0;
	count = 0;
	while (s1[x] != '\0')
		x++;
	while (s2[count] != '\0')
	{
		s1[x] = s2[count];
		count++;
		x++;
	}
	s1[x] = '\0';
	return (s1);
}
