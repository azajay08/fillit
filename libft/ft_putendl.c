#include "libft.h"

void	ft_putendl(char const *s)
{
	int	x;

	x = 0;
	if (!s)
		return ;
	while (s[x] != '\0')
	{
		ft_putchar (s[x]);
		x++;
	}
	ft_putchar ('\n');
}
